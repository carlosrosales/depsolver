#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int shape_tria3(double *L, double *N)
{
    /* Shape function definition for node order 1-2-3 */
    N[0] = L[0];
    N[1] = L[1];
    N[2] = 1.0-L[0]-L[1];

    return 0;
}

